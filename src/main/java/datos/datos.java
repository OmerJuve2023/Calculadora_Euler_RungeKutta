package datos;

public class datos {
    private String funcion;
    private double h;
    private double x1;
    private double y1;
    private int N;

    public datos() {
    }

    public String getFuncion() {
        return funcion;
    }

    public void setFuncion(String funcion) {
        this.funcion = funcion;
    }

    public double getH() {
        return h;
    }

    public void setH(double h) {
        this.h = h;
    }

    public double getX1() {
        return x1;
    }

    public void setX1(double x1) {
        this.x1 = x1;
    }

    public double getY1() {
        return y1;
    }

    public void setY1(double y1) {
        this.y1 = y1;
    }

    public int getN() {
        return N;
    }

    public void setN(int n) {
        N = n;
    }

}
